// Match an article to the CSS past paper questions database
export function matchPastQuestions(title, content) {
  if (!content) return null;
  const text = `${title} ${content}`.toLowerCase();
  
  let bestMatch = null;
  let maxMatchedCount = 0;
  
  CSS_PAST_QUESTIONS.forEach(item => {
    let matchedCount = 0;
    item.keywords.forEach(keyword => {
      if (text.includes(keyword.toLowerCase())) {
        matchedCount++;
      }
    });
    // Require at least 2 keyword matches to establish a valid match
    if (matchedCount >= 2 && matchedCount > maxMatchedCount) {
      maxMatchedCount = matchedCount;
      bestMatch = item;
    }
  });
  
  return bestMatch;
}

// Map article content to CSS Papers & Syllabus Topics
export function mapSyllabus(title, content) {
  const text = `${title} ${content}`.toLowerCase();
  
  // First, check if there is a matched past question to extract the paper/syllabus area
  const matched = matchPastQuestions(title, content);
  if (matched) {
    let topicName = matched.question;
    if (topicName.length > 55) {
      topicName = topicName.substring(0, 52) + "...";
    }
    return {
      paper: matched.paper,
      topic: `Exam ${matched.year}: ${topicName}`
    };
  }

  // Fallback to keyword-based taxonomy mapping
  // 1. Economics & Fiscal Policy
  if (
    text.includes("imf") || text.includes("fiscal") || text.includes("debt") || 
    text.includes("deficit") || text.includes("economy") || text.includes("tax") || 
    text.includes("privatisation") || text.includes("soes") || text.includes("fbr") ||
    text.includes("sifc") || text.includes("balance-of-payments")
  ) {
    return {
      paper: "Pakistan Affairs / Economics",
      topic: "Economic Challenges, IMF Programs & Structural Reforms"
    };
  }
  
  // 2. Climate & Environment
  if (
    text.includes("climate") || text.includes("flood") || text.includes("water") || 
    text.includes("environment") || text.includes("pollution") || text.includes("smog") ||
    text.includes("afforestation") || text.includes("renewable energy") ||
    text.includes("cop27") || text.includes("cop28") || text.includes("cop29") ||
    text.includes("transboundary") || text.includes("climate diplomacy")
  ) {
    return {
      paper: "English Essay / General Science & Ability",
      topic: "Climate Change, Water Scarcity & Energy Security"
    };
  }
  
  // 3. Foreign Policy & IR
  if (
    text.includes("foreign policy") || text.includes("china") || text.includes("us-") || 
    text.includes("bilateral") || text.includes("geopolitics") || text.includes("strategic") || 
    text.includes("diplomacy") || text.includes("cpec") || text.includes("durand") ||
    text.includes("regional connectivity") || text.includes("hegemony") ||
    text.includes("brics") || text.includes("quad") || text.includes("aukus") ||
    text.includes("indo-pacific") || text.includes("containment")
  ) {
    return {
      paper: "International Relations / Current Affairs",
      topic: "Pakistan's Foreign Policy, CPEC & Regional Geopolitics"
    };
  }
  
  // 4. Constitution & Devolution
  if (
    text.includes("constitutional") || text.includes("judicial") || text.includes("amendment") || 
    text.includes("court") || text.includes("federalism") || text.includes("nfc") || 
    text.includes("democracy") || text.includes("devolution") || text.includes("local government") ||
    text.includes("18th amendment") || text.includes("new provinces")
  ) {
    return {
      paper: "Pakistan Affairs / Constitutional Law",
      topic: "Constitutional Evolution, 18th Amendment & Local Governance"
    };
  }

  // 5. Internal Security & Counter-Terrorism
  if (
    text.includes("terrorism") || text.includes("security") || text.includes("militant") || 
    text.includes("extremism") || text.includes("radicalisation") || text.includes("counter-terror") ||
    text.includes("national action plan") || text.includes("asymmetric") ||
    text.includes("hybrid warfare") || text.includes("fifth gen warfare") || text.includes("nuclear delivery")
  ) {
    return {
      paper: "Current Affairs / Internal Security",
      topic: "National Security, Asymmetric Warfare & Counter-Extremism"
    };
  }
  
  // 6. Sociology, Education & Gender
  if (
    text.includes("gender") || text.includes("women") || text.includes("education") || 
    text.includes("poverty") || text.includes("youth") || text.includes("human rights") ||
    text.includes("literacy") || text.includes("stunting") || text.includes("out-of-school") ||
    text.includes("demographic dividend")
  ) {
    return {
      paper: "English Essay / Sociology & Gender Studies",
      topic: "Socio-Cultural Challenges, Youth Bulge & Human Rights"
    };
  }

  // Default fallback
  return {
    paper: "Current Affairs (General Paper)",
    topic: "National Governance & Institutional Reforms"
  };
}

// Heuristic Scoring Engine
export function calculateRelevanceScore(title, author, content) {
  if (!content) return 0;
  
  const textLower = content.toLowerCase();
  const titleLower = title.toLowerCase();
  const fullText = `${titleLower} ${textLower}`;
  
  // 1. Keyword Score (KS) - Weight: 40%
  // Capped at 100, requiring only 2 keyword matches for full points on short snippets
  let matchedKeywords = 0;
  CSS_TAXONOMY.keywords.forEach(keyword => {
    if (fullText.includes(keyword)) {
      matchedKeywords++;
    }
  });
  const KS = Math.min(100, (matchedKeywords / 2) * 100);
  
  // 2. Author Score (AS) - Weight: 20%
  let AS = 0;
  if (author) {
    const authorLower = author.toLowerCase();
    const isWatchlist = CSS_TAXONOMY.authors.some(watchAuthor => 
      authorLower.includes(watchAuthor.toLowerCase())
    );
    if (isWatchlist) {
      AS = 100;
    }
  }
  
  // 3. Depth Score (DS) - Weight: 20%
  // Capped at 100, requiring only 3 transition words/phrases for full depth points
  let matchedTransitions = 0;
  CSS_TAXONOMY.transitions.forEach(phrase => {
    const regex = new RegExp(phrase, 'gi');
    const matches = fullText.match(regex);
    if (matches) {
      matchedTransitions += matches.length;
    }
  });
  const DS = Math.min(100, (matchedTransitions / 3) * 100);
  
  // 4. Solution Score (SS) - Weight: 20%
  let SS = 0;
  const totalLength = content.length;
  const splitIndex = Math.floor(totalLength * 0.7); // Last 30%
  const conclusionText = textLower.substring(splitIndex);
  
  let foundInConclusion = false;
  let foundElsewhere = false;
  
  CSS_TAXONOMY.solutions.forEach(word => {
    if (conclusionText.includes(word)) {
      foundInConclusion = true;
    } else if (textLower.includes(word)) {
      foundElsewhere = true;
    }
  });
  
  if (foundInConclusion) {
    SS = 100;
  } else if (foundElsewhere) {
    SS = 50;
  } else {
    SS = 0;
  }
  
  // Calculate final score: 30 point baseline, scaling the weighted criteria to fill the remaining 70 points
  const rawScore = (KS * 0.40) + (AS * 0.20) + (DS * 0.20) + (SS * 0.20);
  let finalScore = 30 + Math.round(rawScore * 0.70);
  
  // Past paper match bonus: Add +15 relevance bonus if article directly maps to an exam question
  const hasPastPaperMatch = matchPastQuestions(title, content) !== null;
  if (hasPastPaperMatch) {
    finalScore += 15;
  }
  
  return Math.min(100, finalScore);
}
